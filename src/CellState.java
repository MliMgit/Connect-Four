/**
 * Contains the possible colours/states a cell in a {@link Board} can have/be in.
 *
 * @author Mlim
 * @version 1.0
 */
public enum CellState {
    /**
     * The colour red, represented by R
     */
    R,
    /**
     * The colour yellow, represented by Y
     */
    Y,
    /**
     * The state empty, represented by O
     */
    O
}
